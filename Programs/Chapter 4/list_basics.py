# lists

food = ["pizza", "burger", "pasta", "mango", "ice cream"]

print(len(food))  # length of list

print("First element:", food[0])  # first element
print("4th element:", food[3])  # 4th element
print("Last element:", food[-1])  # last element
print("First element using negative index:", food[-5])  # first element using negative index