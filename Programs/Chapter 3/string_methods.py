str = "hElLo, woRlD!"

print("Original String:", str)
print("Uppercase:", str.upper())
print("Lowercase:", str.lower())
print("Title Case:", str.title())   
print("Find 'o' in str:", str.find('o'))
print("Count of 'l' in str:", str.count('l'))
print("Replace 'woRlD' with 'Python':", str.replace('woRlD', 'Python'))
print("Split str by comma:", str.split(','))
print("Replace spaces, comma with underscores:", str.replace(', ', '_'))

print("Hello \n World!")
